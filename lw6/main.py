from hash_table import HashTable

ht = HashTable()

sports = [
    ("Футбол", "Игра 11x11 с мячом"),
    ("Баскетбол", "Игра с кольцами"),
    ("Волейбол", "Игра через сетку"),
    ("Хоккей", "Игра на льду"),
    ("Теннис", "Игра с ракетками"),
    ("Бокс", "Кулачный бой"),
    ("Плавание", "Водный спорт"),
    ("Гимнастика", "Упражнения"),
    ("Биатлон", "Лыжи и стрельба"),
    ("Регби", "Силовая игра")
]

print("--- Вставка элементов ---")
for name, desc in sports:
    ht.insert(name, desc)
    print(f"Вставлено: '{name}'")

print("\nИсходная таблица:")
# Используем магический метод __str__ для печати
print(ht)
print(f"Количество элементов: {len(ht)}")
print(f"Размер таблицы (количество 'корзин'): {ht.size}")
print(f"Коэффициент загрузки: {ht.load_factor}")  # Использование свойства load_factor
print(f"Все ключи: {ht.keys}")
print(f"Все пары ключ-значение: {ht.items}")

print("\n--- Удаление элемента 'Волейбол' ---")
try:
    ht.remove("Волейбол")
    print("Элемент 'Волейбол' успешно удален.")
except KeyError as e:
    print(e)

print("\nТаблица после удаления Волейбола:")
print(ht)
print(f"Количество элементов: {len(ht)}")
print(f"Коэффициент загрузки: {ht.load_factor}")

print("\n--- Обновление элемента 'Футбол' ---")
# Так как insert обновляет значение, если ключ уже существует
ht.insert("Футбол", "Международный футбол: 11 игроков, мяч, ворота")
print("Описание 'Футбол' обновлено.")

print("\nТаблица после обновления Футбола:")
print(ht)
print(f"Количество элементов: {len(ht)}")
print(f"Коэффициент загрузки: {ht.load_factor}")

print("\n--- Получение элементов ---")
print(f"Описание 'Баскетбол': {ht.get('Баскетбол')}")
print(f"Описание 'Хоккей': {ht['Хоккей']}")  # Использование синтаксиса словаря

print("\n--- Проверка наличия элементов ---")
print(f"Содержит ли таблица 'Теннис'? {ht.contains('Теннис')}")
print(
    f"Содержит ли таблица 'Волейбол' (после удаления)? {'Волейбол' in ht}")  # Использование магического метода __contains__

print("\n--- Попытка удалить несуществующий элемент ---")
try:
    ht.remove("Кёрлинг")
except KeyError as e:
    print(e)

print("\n--- Попытка получить несуществующий элемент ---")
print(f"Описание 'Лыжи': {ht.get('Лыжи')}")
